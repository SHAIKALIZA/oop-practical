package oopspractical;

public class bitwiseshifts {
	public static void main(String args[]) {
	      int a = 60;	
	      int c = 0;

	      c = a << 2;      
	      System.out.println("a << 2 = " + c );

	      c = a >> 2;      
	      System.out.println("a >> 2  = " + c );

	      c = a >>> 2;   
	      System.out.println("a >>> 2 = " + c );
	   }
	}